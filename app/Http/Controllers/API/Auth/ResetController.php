    {
        $email = $request->email;
        $phone = $request->phone;

        if ($email == '') {
            $user = User::where('email', $email)->first();
        } else {
            $user = User::where('phone', $phone)->first();   
        }
        
        return response()->json([
            'status' => 'Success',
            'message' => 'In progressing',
            'data' => [
                'user' => $user,
